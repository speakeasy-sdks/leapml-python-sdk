__doc__ = """ SDK Documentation: The Official Leap API"""
import requests as requests_http
from . import utils
from .fine_tuning import FineTuning
from .generating_images import GeneratingImages
from .image_editing import ImageEditing

SERVERS = [
	"https://api.tryleap.ai",
]

class LeapMLAPI:
    r"""SDK Documentation: The Official Leap API"""
    fine_tuning: FineTuning
    generating_images: GeneratingImages
    image_editing: ImageEditing
    
    _client: requests_http.Session
    _security_client: requests_http.Session
    _server_url: str = SERVERS[0]
    _language: str = "python"
    _sdk_version: str = "1.10.2"
    _gen_version: str = "1.12.3"

    def __init__(self,
                 server_url: str = None,
                 url_params: dict[str, str] = None,
                 client: requests_http.Session = None
                 ) -> None:
        self._client = requests_http.Session()
        
        
        if server_url is not None:
            if url_params is not None:
                self._server_url = utils.template_url(server_url, url_params)
            else:
                self._server_url = server_url

        if client is not None:
            self._client = client
        
        self._security_client = self._client
        

        self._init_sdks()
    
    def _init_sdks(self):
        self.fine_tuning = FineTuning(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.generating_images = GeneratingImages(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
        self.image_editing = ImageEditing(
            self._client,
            self._security_client,
            self._server_url,
            self._language,
            self._sdk_version,
            self._gen_version
        )
        
    